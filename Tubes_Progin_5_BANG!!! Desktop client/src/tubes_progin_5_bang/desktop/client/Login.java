    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        //LOGIN COY
        try {
            connectToServer(serverHostname);
            PrintWriter out = new PrintWriter(echoSocket.getOutputStream(), true);
            BufferedReader in = new BufferedReader(new InputStreamReader(echoSocket.getInputStream()));
            System.out.println(new String(jPasswordField1.getPassword()));
            out.println("login;" + jTextField1.getText() + ";" + new String(jPasswordField1.getPassword()));
            String response = in.readLine();
            switch (response) {
                case "success":
                    username = jTextField1.getText();
                    System.out.println("Echo Socket : " + echoSocket);
                    System.out.println("You have been successfully logged in");
                    this.setVisible(false);
                    ClientForm temp = new ClientForm();
                    temp.setUsername(username);
                    temp.setEchoSocket(echoSocket);
                    temp.setVisible(true);
                    dispose();
                    break;
                case "failed":
                    System.out.println("Username or password is wrong");
                    break;
            }
//            in.close();
//            out.close();
//            echoSocket.close();
        } catch (IOException ex) {
            System.out.println("Can't log in to server");
        }
    }//GEN-LAST:event_jButton1MousePressed

